    public boolean isValidate() {
        if (txtma.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Chua nhap ma");
            return false;
        }
        if (txt_dt.getText().isEmpty() || txt_ten.getText().isEmpty() || txt_ngsinh.getText().isEmpty() ||
                txt_diachi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Không được để trống");
            return false;
        }
        return true;
    }
