    public boolean isValidate() {
        if (txtTenHang.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Chua nhap ten hang");
            return false;
        }
        if(txtTenHang.getText().trim().matches(("-?\\d+(\\.\\d+)?"))){
            JOptionPane.showMessageDialog(this, "Sai kieu man hinh");
            return false;
        }
        return true;
    }
