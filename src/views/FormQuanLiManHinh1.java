    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        // TODO add your handling code here:
        if (!isValidate()) {
            return;
        }
        int key = list.get(index).getMaManHinh();
        Integer x = mhsv.update(readForm(),key);
        if( x != null){
            JOptionPane.showMessageDialog(this, "Sửa thành công");
            page =1;
            list= mhsv.paging(1, 2);
            loadDataToTable();
        }else JOptionPane.showMessageDialog(this, "Sửa thất bại");
    }//GEN-LAST:event_btnSuaActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int chon = JOptionPane.showConfirmDialog(this, "Bạn Chắc chắn muốn xóa chứ?");
        if(chon!=0){
            return;
        }
        Integer x = mhsv.delete(list.get(index).getMaManHinh());
        if( x != null){
            JOptionPane.showMessageDialog(this, "Xóa thành công");
            page=1;
            list= mhsv.paging(1, 2);
            loadDataToTable();
        }else JOptionPane.showMessageDialog(this, "Xóa thất bại");
        
    }//GEN-LAST:event_btnXoaActionPerformed

