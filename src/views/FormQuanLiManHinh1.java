    private void loadDataToTable() {
        model.setRowCount(0);
        list = mhsv.searchByTrangThai(page, 2, textsearch);
        for(ManHinh x : list){
            model.addRow(new Object[]{x.getMaManHinh(),
                x.getTenManHinh(),x.getTrangThai()});
        }
    }
    
    public void showDetail(){
        txtTenHang.setText((String) model.getValueAt(index, 1));
        cbbTrangThai.setSelectedItem(model.getValueAt(index, 2));
    }
    
    public ManHinh readForm(){
        ManHinh x = new ManHinh();
        x.setTenManHinh(txtTenHang.getText());
        x.setTrangThai((String)cbbTrangThai.getSelectedItem());
        return x;
    }
